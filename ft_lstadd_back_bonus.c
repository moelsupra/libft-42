#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)//head
{
	t_list	*last;

	if (!lst || !new)
		return ;
	if (*lst == NULL)
	{
		*lst = new;
		return ;
	}
	last = ft_lstlast(*lst);
	last->next = new;
}
