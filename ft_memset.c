#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*pointer;
	unsigned char	uc;

	uc = (unsigned char)c;
	pointer = (unsigned char *) b;
	while (len)
		pointer[--len] = uc ;
	return (b);
}

