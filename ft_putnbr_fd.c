 #include <unistd.h>

void ft_putnbr_fd(int n, int fd)
{
    if (n == -2147483648)
    {
        write(fd, "-2147483648", 11);
        return;
    }
    if (n < 0)
    {
        n *= -1;
        write(fd, "-", 1);
    }
    if (n >= 9)
        putnbr(n/10);
    n = n % 10 + '0';
    write(fd, &n, 1);   
}
